#include "HillBiome.hpp"
namespace planet {

void HillBiome::onGenerateStructures(BlockTable& blockTable) {
        MultiBlock woord =
            MultiBlock::fromFile("./data/block/woord.multiblock");
        generateStructure(blockTable, woord, glm::ivec3(3, 0, 12),
                          glm::ivec3(3, 0, 12), 100, 20);
}
}  // namespace planet
